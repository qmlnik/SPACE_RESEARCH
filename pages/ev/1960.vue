                és 1962 szeptember 5-én semmisült  meg a légkörben.
                Érdekesség, hogy egyik darabkája a Wisconsin állambeli Manitowoc-ban landolt.
                Egy 20 cm-es darab az aszfaltba fúródott, amit később visszaszolgáltattak a Szovjetúniónak.
                A becsapódás emlékét egy fémkarika (pontosan ott csapódott be), egy emléktábla és egy fesztivál
                őrzi (Sputnikfest néven).
            </p>
