        <h2 class="mb-4">Cél: embert az űrbe</h2>
        <p>
            Miután az űrverseny első köre eldőlt a Szputnyik-1-el, a nagyhatalmak új célja
            az első ember űrbe juttatása lett. Erre a célra Amerikában a Mercury-programot (1958), a Szovjetúnióban
            a Vosztok-programot (1961) indították el. Utóbbinál az előkészítő küldetéseket a
            Szputnyik-program részeként végezték el, az ilyen küldetések Korabl-Szputnyik név alatt futottak.
        </p>
