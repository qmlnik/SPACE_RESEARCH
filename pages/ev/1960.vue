            <p>
                Az 1950-es években óriási volt a nyomás Amerikán, miután a Szovjetúnió kifejlesztette a hidrogénbombát
                és az őket célba juttatni képes bombázóit és ICBM-jeit.
                A hírszerzés legfőbb eszköze először a Lockheed Martin cég által készített U-2-es kémrepülő volt,
                ami mélyen be tudott repülni a Szovjetúnió területére.
                Első bevetése 1956-ban volt, a több mint 20 km-es repülési magassága miatt hiába tudták radaron követni,
                a szovjet légvédelem nem tudta lelőni.
                Ez 1960 május 1-ig maradt így.
                A pilótát elfogták és két évig munkatárborban fogva tartották, ami után egy
                fogolycserével tudott szabadulni.
                Az eset új löketet adott az akkor még korai fázisban lévő CORONA műholdas felderítő programnak.
            </p>
