            <p>
                A CORONA program volt az USA fő műholdas felderítő programja, ami 1955-től 1972-ig tartott.
                A műholdakon analóg kamerák voltak, amiknek a felvételeit kapszulákba téve
                juttatták vissza a Földre.
                Néhány színes és infravörös filmet leszámítva a filmek fekete-fehérek voltak.
                A kapszulát 18 km-es magasság alatt ejtőernyőkkel lassították,
                ha lehetett speciális repülőgéppel kapták el a levegőben.
                Ha ez nem sikerült, akkor még a víz felszínén vártak a begyűjtésre,
                két nap után azonban automatikusan elsüllyedtek, mivel az aljára elhelyezett
                sóból készült dugó feloldódott a vízben.
                Az első sikert 1960 augusztus 11-én a Discoverer-13 hozta meg, aminél elsőként sikerült begyűjteni az
                általa ledobott kapszulát, azonban nem volt rajta kamera, így a kapszula üres volt.
                Ez volt a világon az első ember alkotta tárgy, ami épségben visszatért a világűrből
                (9 nappal megelőzve a Szputnyik-5-öt).
                A teljes sikert pedig a Discoverer-14 hozta meg, ami először juttatott vissza épségben
                filmeket a Földre.
                A kilövés augusztus 18-án történt és egy napra rá küldte vissza a kapszulát.
                Ez az egy küldetés több felvétellel szolgált, mint az összes U-2-es repülés.
                A Discoverer-14-et egy Thor-Agena A rakéta juttatta az űrbe, a műhold
                az Agena orr észében kapott helyett, 850 kg-ot nyomott és 27 órás keringés után
                Hawaii közelében kapták el kb. 2 500 m-es magasságban.
            </p>
